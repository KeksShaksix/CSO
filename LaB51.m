% Применение фильтра к сигналу (пример для фильтра Баттерворта)
[b, a] = butter(4, 0.2);  % Создание фильтра Баттерворта
y = filter(b, a, x);       % Фильтрация сигнала

% Визуализация выходного сигнала
figure;
subplot(2,1,1);
plot(t, x);
title('Входной сигнал');
xlabel('Время (с)');
ylabel('Амплитуда');
grid on;

subplot(2,1,2);
plot(t, y);
title('Выходной сигнал после фильтрации');
xlabel('Время (с)');
ylabel('Амплитуда');
grid on;
